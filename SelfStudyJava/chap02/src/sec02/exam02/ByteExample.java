public class ByteExample {

	public static void main(String[] args) {
		// byte : from -128 to 127
		byte var1 = -128;
		byte var2 = -30;
		byte var3 = 024;
		byte var4 = 127;
		
		System.out.println("var1 : " + var1);
		System.out.println("var2 : " + var2);
		System.out.println("var3 : " + var3);
		System.out.println("var4 : " + var4);
	}

}
